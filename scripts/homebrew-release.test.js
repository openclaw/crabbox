function formula(overrides = {}, { literalUrls = true } = {}) {
  const values = { ...hashes, ...overrides };
  const releaseUrl = (platform, arch) =>
    literalUrls
      ? `https://github.com/openclaw/crabbox/releases/download/v1.2.3/crabbox_1.2.3_${platform}_${arch}.tar.gz`
      : `https://github.com/openclaw/crabbox/releases/download/v#{version}/crabbox_#{version}_${platform}_${arch}.tar.gz`;
  return `# typed: false
# frozen_string_literal: true

# Generated by the protected Crabbox release tooling. DO NOT EDIT.
class Crabbox < Formula
  desc "Remote software testing and execution"
  homepage "https://github.com/openclaw/crabbox"
  version "1.2.3"
  license "MIT"

  on_macos do
    if Hardware::CPU.intel?
      url "${releaseUrl("darwin", "amd64")}"
      sha256 "${values.darwinAmd64}"

      define_method(:install) do
        bin.install "crabbox"
        bin.install "crabbox-apple-vm-helper" if OS.mac? && Hardware::CPU.arm?
      end
    end
    if Hardware::CPU.arm?
      url "${releaseUrl("darwin", "arm64")}"
      sha256 "${values.darwinArm64}"

      define_method(:install) do
        bin.install "crabbox"
        bin.install "crabbox-apple-vm-helper" if OS.mac? && Hardware::CPU.arm?
      end
    end
  end

  on_linux do
    if Hardware::CPU.intel? && Hardware::CPU.is_64_bit?
      url "${releaseUrl("linux", "amd64")}"
      sha256 "${values.linuxAmd64}"
      define_method(:install) do
        bin.install "crabbox"
        bin.install "crabbox-apple-vm-helper" if OS.mac? && Hardware::CPU.arm?
      end
    end
    if Hardware::CPU.arm? && Hardware::CPU.is_64_bit?
      url "${releaseUrl("linux", "arm64")}"
      sha256 "${values.linuxArm64}"
      define_method(:install) do
        bin.install "crabbox"
        bin.install "crabbox-apple-vm-helper" if OS.mac? && Hardware::CPU.arm?
      end
    end
  end

  test do
    system bin/"crabbox", "--version"
  end
end
`;
}

function verifyFormula(contents) {
  const root = fs.mkdtempSync(path.join(os.tmpdir(), "crabbox-homebrew-formula-"));
  const formulaPath = path.join(root, "crabbox.rb");
  fs.writeFileSync(formulaPath, contents);
  try {
    return spawnSync(
      "/bin/bash",
      [
        "-c",
        'source "$1"; verify_homebrew_formula "$2" "$3" v1.2.3 "$4" "$5" "$6" "$7"',
        "homebrew-formula-test",
        verifier,
        process.execPath,
        formulaPath,
        hashes.darwinAmd64,
        hashes.darwinArm64,
        hashes.linuxAmd64,
        hashes.linuxArm64,
      ],
      { encoding: "utf8" },
    );
  } finally {
    fs.rmSync(root, { recursive: true, force: true });
  }
}

function sha256(file) {
  return createHash("sha256").update(fs.readFileSync(file)).digest("hex");
}

function shellQuote(value) {
  return `'${String(value).replaceAll("'", `'"'"'`)}'`;
}

function writeExecutable(file, contents) {
  fs.writeFileSync(file, contents);
  fs.chmodSync(file, 0o755);
}

