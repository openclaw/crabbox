test("formula verifier accepts only the exact four frozen archive mappings", () => {
  const exactCanonical = verifyFormula(formula());
  assert.equal(exactCanonical.status, 0, exactCanonical.stderr);

  const interpolationTemplate = verifyFormula(formula({}, { literalUrls: false }));
  assert.notEqual(interpolationTemplate.status, 0);
  assert.match(interpolationTemplate.stderr, /exact protected canonical program/);

  const wrongHash = verifyFormula(formula({ darwinArm64: "e".repeat(64) }));
  assert.notEqual(wrongHash.status, 0);
  assert.match(wrongHash.stderr, /exact protected canonical program/);

  const wrongContext = verifyFormula(
    formula().replace(
      "crabbox_1.2.3_darwin_amd64.tar.gz",
      "crabbox_1.2.3_linux_amd64.tar.gz",
    ),
  );
  assert.notEqual(wrongContext.status, 0);
  assert.match(wrongContext.stderr, /exact protected canonical program/);

  const extraTarget = verifyFormula(
    formula().replace(
      "  test do",
      `  on_macos do
    if Hardware::CPU.intel?
      url "https://example.test/extra.tar.gz"
      sha256 "${"f".repeat(64)}"
    end
  end

  test do`,
    ),
  );
  assert.notEqual(extraTarget.status, 0);
  assert.match(extraTarget.stderr, /exact protected canonical program/);

  const arbitraryRuby = verifyFormula(
    formula().replace(
      '  homepage "https://github.com/openclaw/crabbox"',
      '  homepage "https://github.com/openclaw/crabbox"\n  system "/usr/bin/touch", "/tmp/should-not-run"',
    ),
  );
  assert.notEqual(arbitraryRuby.status, 0);
  assert.match(arbitraryRuby.stderr, /exact protected canonical program/);

  const unexpectedInterpolation = verifyFormula(
    formula().replace("/download/v1.2.3/", "/download/v#{version}/"),
  );
  assert.notEqual(unexpectedInterpolation.status, 0);
  assert.match(unexpectedInterpolation.stderr, /exact protected canonical program/);

  const rubyExpression = verifyFormula(
    formula().replace(
      'url "https://github.com/openclaw/crabbox/releases/download/v1.2.3/crabbox_1.2.3_darwin_amd64.tar.gz"',
      'url "https://github.com/openclaw/crabbox/releases/download/v" + version',
    ),
  );
  assert.notEqual(rubyExpression.status, 0);
  assert.match(rubyExpression.stderr, /exact protected canonical program/);
});

