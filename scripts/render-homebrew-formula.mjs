#!/usr/bin/env node

const [tag, darwinAmd64, darwinArm64, linuxAmd64, linuxArm64] = process.argv.slice(2);
if (!/^v(?:0|[1-9][0-9]*)\.(?:0|[1-9][0-9]*)\.(?:0|[1-9][0-9]*)$/.test(tag ?? "")) {
  throw new Error("invalid release tag");
}
for (const digest of [darwinAmd64, darwinArm64, linuxAmd64, linuxArm64]) {
  if (!/^[0-9a-f]{64}$/.test(digest ?? "")) throw new Error("invalid release archive digest");
}
const version = tag.slice(1);
const url = (platform, arch) =>
  `https://github.com/openclaw/crabbox/releases/download/${tag}/crabbox_${version}_${platform}_${arch}.tar.gz`;

process.stdout.write(`# typed: false
# frozen_string_literal: true

# Generated by the protected Crabbox release tooling. DO NOT EDIT.
class Crabbox < Formula
  desc "Remote software testing and execution"
  homepage "https://github.com/openclaw/crabbox"
  version "${version}"
  license "MIT"

  on_macos do
    if Hardware::CPU.intel?
      url "${url("darwin", "amd64")}"
      sha256 "${darwinAmd64}"

      define_method(:install) do
        bin.install "crabbox"
        bin.install "crabbox-apple-vm-helper" if OS.mac? && Hardware::CPU.arm?
      end
    end
    if Hardware::CPU.arm?
      url "${url("darwin", "arm64")}"
      sha256 "${darwinArm64}"

      define_method(:install) do
        bin.install "crabbox"
        bin.install "crabbox-apple-vm-helper" if OS.mac? && Hardware::CPU.arm?
      end
    end
  end

  on_linux do
    if Hardware::CPU.intel? && Hardware::CPU.is_64_bit?
      url "${url("linux", "amd64")}"
      sha256 "${linuxAmd64}"
      define_method(:install) do
        bin.install "crabbox"
        bin.install "crabbox-apple-vm-helper" if OS.mac? && Hardware::CPU.arm?
      end
    end
    if Hardware::CPU.arm? && Hardware::CPU.is_64_bit?
      url "${url("linux", "arm64")}"
      sha256 "${linuxArm64}"
      define_method(:install) do
        bin.install "crabbox"
        bin.install "crabbox-apple-vm-helper" if OS.mac? && Hardware::CPU.arm?
      end
    end
  end

  test do
    system bin/"crabbox", "--version"
  end
end
`);
